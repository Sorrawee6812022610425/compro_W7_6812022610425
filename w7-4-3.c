/*if-else if:  -อ่านง่าย เหมาะกับเงื่อนไขที่มีการเปรียบเทียบซับซ้อน
               -เพิ่มระดับสมาชิกใหม่ได้ง่ายโดยเพิ่ม else if
               -เหมาะกับกรณีที่มีเงื่อนไขซับซ้อนหรือช่วงของค่า*/

/*switch-case: -โค้ดกระชับ เหมาะกับค่าที่เป็นตัวเลขหรือค่าคงที่
               -เพิ่มระดับสมาชิกใหม่ได้ง่ายโดยเพิ่ม case
               -เหมาะกับกรณีที่มีตัวเลือกชัดเจนและไม่ซับซ้อน*/

/*สรุป : สำหรับระบบนี้ที่ระดับสมาชิกเป็นตัวเลขและแต่ละระดับมีสิทธิประโยชน์เฉพาะเจาะจง switch-case จะเหมาะสมกว่า เพราะโค้ดกระชับและดูแลรักษาง่าย 
หากเพิ่มระดับใหม่ก็เพิ่ม case ได้ทันทีแต่ถ้าต้องการเงื่อนไขที่ซับซ้อนหรือช่วงของค่า ให้ใช้ if-else if*/


